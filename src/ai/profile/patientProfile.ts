// src/ai/profile/patientProfile.ts

// Главный профиль пациента.
//
// Это ЕДИНЫЙ источник правды для AI.
//
// GPT больше НЕ должен анализировать:
// - сырой чат,
// - кашу из extractedFacts,
// - случайные сообщения.
//
// Вместо этого GPT получает
// структурированный профиль пациента.
//
// Этот профиль:
// - динамически обновляется,
// - хранит уже известные данные,
// - помогает AI не повторяться,
// - помогает двигаться по диагностике,
// - делает reasoning стабильным.
//
// ВАЖНО:
// Здесь только структура.
// Логику обновления будем делать отдельно.

export interface PatientProfile {

  // -----------------------------------
  // ОСНОВНАЯ ЖАЛОБА
  // -----------------------------------

  mainComplaint: string;

  // -----------------------------------
  // ОСНОВНЫЕ СИМПТОМЫ
  // -----------------------------------

  symptoms: string[];

  // -----------------------------------
  // БОЛЬ
  // -----------------------------------

  pain: {

    // Где болит
    location: string;

    // Характер боли:
    // тупая, острая, жгучая и т.д.
    character: string;

    // Сила боли:
    // слабая, средняя, сильная
    severity: string;

    // Как давно болит
    duration: string;
  };

  // -----------------------------------
  // ТРАВМА
  // -----------------------------------

  trauma: {

    exists: boolean;

    mechanism: string;
  };

  // -----------------------------------
  // ОГРАНИЧЕНИЯ
  // -----------------------------------

  functionalLimitations: string[];

  // -----------------------------------
  // ВОЗМОЖНЫЕ ТРИГГЕРЫ
  // -----------------------------------

  possibleTriggers: string[];

  // -----------------------------------
  // ЧТО УЖЕ ПРОБОВАЛ
  // -----------------------------------

  treatmentsTried: string[];

  medicationsTried: string[];

  // -----------------------------------
  // RED FLAGS
  // -----------------------------------

  redFlags: string[];

  // -----------------------------------
  // ЧТО УЖЕ ВЫЯСНЕНО
  // -----------------------------------

  resolvedTopics: string[];

  // -----------------------------------
  // ЧЕГО НЕ ХВАТАЕТ
  // -----------------------------------

  missingTopics: string[];

  // -----------------------------------
  // ВЕРОЯТНЫЕ СЦЕНАРИИ
  // -----------------------------------

  likelyScenarios: string[];

  // -----------------------------------
  // ИСКЛЮЧЕННЫЕ СЦЕНАРИИ
  // -----------------------------------

  excludedScenarios: string[];
}

